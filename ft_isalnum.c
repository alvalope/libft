int	ft_isalnum(int c)
{
	int	isalnum;

	if (c < '0' || (c > '9' && c < 'A') || (c > 'Z' && c < 'a') || c > 'z')
		isalnum = 0;
	else
		isalnum = 1;
	return (isalnum);
}
